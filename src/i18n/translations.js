export function getTranslationValue(language, key) {
  return key.split('.').reduce((value, part) => (value && value[part] !== undefined ? value[part] : undefined), translations[language])
}

export function interpolateTranslation(value, params = {}) {
  if (typeof value !== 'string') {
    return value
  }

  return value.replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? ''))
}

export function translateAnimalType(animalType, language) {
  return ANIMAL_TYPE_TRANSLATIONS[animalType]?.[language] ?? animalType
}

export function translateCategory(category, language) {
  return CATEGORY_TRANSLATIONS[category]?.[language] ?? category
}

export function localizeProduct(product, language) {
  if (!product || language === 'ru') {
    return product
  }

  return {
    ...product,
    name: translateProductText(product.name, language),
    description: translateProductText(product.description, language),
    specs: Array.isArray(product.specs) ? product.specs.map((item) => translateProductText(item, language)) : [],
    animalType: translateAnimalType(product.animalType, language),
    category: translateCategory(product.category, language),
  }
}

function translateProductText(text, language) {
  if (!text || language === 'ru') {
    return text
  }

  const replacements = PRODUCT_TEXT_REPLACEMENTS[language] ?? []

  return replacements.reduce((currentText, [source, replacement]) => replacePhrase(currentText, source, replacement), text)
}

function replacePhrase(text, source, replacement) {
  const pattern = new RegExp(escapeRegExp(source), 'gi')
  return text.replace(pattern, (match) => preserveCase(match, replacement))
}

function preserveCase(source, target) {
  if (!source) {
    return target
  }

  return source[0] === source[0].toUpperCase() ? capitalize(target) : target
}

function capitalize(value) {
  return value ? `${value[0].toUpperCase()}${value.slice(1)}` : value
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
}
